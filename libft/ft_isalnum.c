#include "libft.h"

int	ft_isalnum(int a)
{
	if (a < 0 || a > 255)
		return (0);
	return ((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z')
		|| (a >= '0' && a <= '9'));
}
